namespace TraceUtils.SourceGenerator.Infrastructure;

/// <summary>
/// Хранит статичные данные для генерации расширений (например: namespace атрибутов, string template и так далее)
/// </summary>
internal static class TelemetryGenerationConstants
{
    /// <summary>
    /// 
    /// </summary>
    public const string ActivityOperationAttributeName = "TraceUtils.ActivityOperationAttribute";

    /// <summary>
    /// 
    /// </summary>
    public const string SpanTagAttributeName = "TraceUtils.SpanTagAttribute";

    /// <summary>
    /// 
    /// </summary>
    public const string GroupedFileTemplate = """
        // <auto-generated/>
        #nullable enable

        using System;
        using System.Diagnostics;
        using System.Threading.Tasks;
        using OpenTelemetry.Trace;
        using {{ContainingNamespace}};

        namespace TraceUtils.Extensions;

        public static class {{ClassName}}TelemetryExtensions
        {
        {{Methods}}
        }
        """;

    /// <summary>
    /// 
    /// </summary>
    public const string ExtensionMethodTemplate = """
        {{signature}}
        {
            using var activity = Utils.StartActivity("{{OperationName}}", ActivityKind.{{ActivityType}});

            if (Utils.ShouldRecordData(activity))
            {
                {{PreCallTags}}
                try
                {
                    activity?.AddEvent(new ActivityEvent("Начало события \"{{OperationName}}\""));
                    {{Call}}
                    {{PostCallTags}}
                }
                catch (Exception ex)
                {
                    activity?.SetStatus(ActivityStatusCode.Error, ex.Message);
                    activity?.AddException(ex);
                    throw;
                }
                finally
                {
                    activity?.AddEvent(new ActivityEvent("Конец события \"{{OperationName}}\""));
                }
            }
            else
            {
                {{Call}}
            }
        }
    """;
}
